	<h2>01. Shipping Regulations - Where does WineYard.com ship wine? </h2>
<p id="top">
<span>We ship WINE to MOST STATES. Use our STATE DROP DOWN list in the billing information page.</span>
To determine if WineYard.com is able to ship wine to the state you would like your order delivered, utilize the state drop down list that is at the top right side of every Wine.com web page. State laws on shipping wine are complex and there are some states that prohibit out-of-state retailers from shipping wine into their state.  If we can't ship wine to your selected state, when you enter that state into the drop down menu, our site will alert you to this and point you to some great non-wine items.
</p>
<p>
<span>There are a few ZIP CODE RESTRICTIONS</span> 
Local laws prevent us from shipping to certain zip codes that are considered "dry zip codes" in Alaska, Florida, Idaho and New Hampshire.  We do ship to most of the zip codes in these states, but our system will tell you if the zip code you enter is one that is excluded by law at the time the order is placed, after the shipping address and zip code is entered.
</p>
<p>
<span>
NEW YORK has additional regulations - NO WINE & FOOD IN THE SAME BOX!</span>
New York regulations prohibit wine and food from being shipped in the same package. Gifts and Baskets ordered that include both food and wine will be sent in two separate packages. Your gift message will be included in both packages and your Order Confirmation will show two order numbers that will also be given to separate tracking numbers once they are shipped.
</p>
