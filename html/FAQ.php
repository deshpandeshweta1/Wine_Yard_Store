<h2>How long will my wine last once it's opened?</h2>

<p>Oxidation—which begins as soon as the cork is pulled—is the enemy of wine. Refrigeration will slow oxidation, but it doesn't stop it altogether. If you transfer the leftovers to a smaller bottle (where less surface area is exposed to oxygen), you might get some extra mileage out of it. </p>
<hr>

<h2>How should I store my wine? </h2>
<p>Do your best to keep the wines away from light, heat, vibration and—most importantly—temperature fluctuation. For most people, this could mean a corner of a closet. Keep the bottles lying on their side so the cork stays moist and doesn't dry out.

If you have a cooling unit, cellar temperature should be maintained at 55° F, with 70 percent humidity and a 10 percent chance of rain. Humidity is good because it's another way to protect from drying, brittle corks that might cause premature aging. Furthermore, there's some concern that vibration or other disturbance of older wines can lead to premature aging.</p>

